/// Tipos de badge disponibles en el sistema de diseño.
enum DSBadgeType {
  /// Badge de éxito (verde)
  success,

  /// Badge de error (rojo)
  error,

  /// Badge de advertencia (amarillo)
  warning,

  /// Badge de información (azul)
  info,

  /// Badge neutral (gris)
  neutral,
}

/// Tamaños de badge disponibles.
enum DSBadgeSize {
  /// Pequeño
  small,

  /// Mediano
  medium,

  /// Grande
  large,
}
