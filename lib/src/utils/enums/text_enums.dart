/// Variantes de texto disponibles en el sistema de diseño.
///
/// Cada variante corresponde a un estilo tipográfico predefinido.
enum DSTextVariant {
  /// Display Large - 57px
  displayLarge,

  /// Display Medium - 45px
  displayMedium,

  /// Display Small - 36px
  displaySmall,

  /// Heading Large - 32px
  headingLarge,

  /// Heading Medium - 28px
  headingMedium,

  /// Heading Small - 24px
  headingSmall,

  /// Title Large - 22px
  titleLarge,

  /// Title Medium - 16px
  titleMedium,

  /// Title Small - 14px
  titleSmall,

  /// Body Large - 16px
  bodyLarge,

  /// Body Medium - 14px (Base)
  bodyMedium,

  /// Body Small - 12px
  bodySmall,

  /// Label Large - 14px
  labelLarge,

  /// Label Medium - 12px
  labelMedium,

  /// Label Small - 11px
  labelSmall,

  /// Caption - 12px
  caption,

  /// Overline - 10px
  overline,
}
